					Testo della legge<br><br>	
					TITOLO I<br>
					DISPOSIZIONI IN MATERIA DI ENTRATA<br><br>
 					Art. 24 <br>
					(Revisione dei meccanismi di adeguamento retributivo per il personale non contrattualizzato)
					</p>
					<p class="small text-justify">
					    1. A decorrere dal 1o gennaio 1998 gli stipendi, l'indennità integrativa speciale e gli assegni fissi e continuativi dei docenti e dei ricercatori universitari, del personale dirigente della Polizia di Stato e gradi di qualifiche corrispondenti, dei Corpi di polizia civili e militari, dei colonnelli e generali delle Forze armate, del personale dirigente della carriera prefettizia, nonchè del personale della carriera diplomatica, sono adeguati di diritto annualmente in ragione degli incrementi medi, calcolati dall'ISTAT, conseguiti nell'anno precedente dalle categorie di pubblici dipendenti contrattualizzati sulle voci retributive, ivi compresa l'indennità integrativa speciale, utilizzate dal medesimo Istituto per l'elaborazione degli indici delle retribuzioni contrattuali.<br>
						2. La percentuale dell'adeguamento annuale prevista dal comma 1 è determinata entro il 30 aprile di ciascun anno con decreto del Presidente del Consiglio dei ministri, su proposta dei Ministri per la funzione pubblica e del tesoro, del bilancio e della programmazione economica. A tal fine, entro il mese di marzo, l'ISTAT comunica la variazione percentuale di cui al comma 1. Qualora i dati necessari non siano disponibili entro i termini previsti, l'adeguamento è effettuato nella stessa misura percentuale dell'anno precedente, salvo successivo conguaglio.<br>
						3. Con il decreto relativo all'adeguamento per l'anno 1999 si provvederà all'eventuale conguaglio tra gli incrementi corrisposti per l'anno 1998 e quelli determinati ai sensi dei commi 1 e 2.<br>
						4. Il criterio previsto dal comma 1 si applica anche al personale di magistratura ed agli avvocati e procuratori dello Stato ai fini del calcolo dell'adeguamento triennale, ferme restando, per quanto non derogato dal predetto comma 1, le disposizioni dell'articolo 2 della legge 19 febbraio 1981, n. 27, tenendo conto degli incrementi medi pro capite del trattamento economico complessivo, comprensivo di quello accessorio e variabile, delle altre categorie del pubblico impiego.<br>
						5. Per l'anno 1998 le disposizioni di cui al presente articolo si applicano anche ai fini dell'adeguamento retributivo dei dirigenti dello Stato incaricati della direzione di uffici dirigenziali di livello generale o comunque di funzioni di analogo livello.<br>
						6. Fino alla data di entrata in vigore dei contratti di cui all'articolo 24 del decreto legislativo 3 febbraio 1993, n. 29, e successive modificazioni e integrazioni, sono prorogate le disposizioni di cui all'articolo 1 della legge 2 ottobre 1997, n. 334. A tal fine è autorizzata la spesa di lire 37 miliardi annue a decorrere dall'anno 1999., Palazzo della Consulta, il 24 giugno 2015.<br>
